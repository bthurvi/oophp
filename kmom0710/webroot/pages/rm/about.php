    <h2> Om Rental Movies </h2>
    <p>Rental movies var från böran en svensk hyrvideokedja. Toppåret 2000 hade vi  300 anställa i över 1 000 videobutiker.</p>
        
    <p>När konkurrensen hårdnade från Netflix och Redbox, gick det allt sämre för de fysika butikerna och den 6 april 2011 köptes företaget och dess kvarvarande 1 700 videobutiker av Evil Network för 1000$. . Dish stängde 20 butiker i juli 2011, 500 till under första halvan av 2012 och ytterligare 300 i 2013. I november 2013 meddelades att de kvarstående 300 butikerna skulle stängas. Vår DVD-uthyrning via-epost upphörde också.i samband med detta.</p>
    
<p>Numera lever RM endast vidare digitalt - här går dock uthyrningen lysande!</p>


